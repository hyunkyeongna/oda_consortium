def recommend_consortium(
    components: List[Dict[str, Any]],
    humanitarian: pd.DataFrame | None,
    nonprofit: pd.DataFrame | None,
    field_partners: List[Dict[str, Any]],
    recipient_country: str = "스리랑카",
    partner_db: Optional[Dict[str, pd.DataFrame]] = None,
) -> Tuple[List[Dict[str, Any]], float, Dict[str, float]]:
    """Recommend one domestic lead/candidate per component and calculate demo score."""
    rec: List[Dict[str, Any]] = []

    for c in components:
        m = match_domestic(c, humanitarian, nonprofit, recipient_country, top_n=5, partner_db=partner_db)
        lead = m.iloc[0].to_dict() if len(m) else None
        fps = [f["name"] for f in field_partners if c.get("id") in f.get("components", [])]
        item = {
            "component": c,
            "국내후보": lead,
            "domestic": lead,
            "국내후보목록": m.to_dict("records"),
            "domestic_candidates": m.to_dict("records"),
            "현지파트너": fps,
            "field": fps,
            "required_capabilities": infer_required_capabilities(c),
        }
        rec.append(item)

    covered = sum(1 for r in rec if r["국내후보"])
    coverage = covered / max(len(components), 1)
    avg_match = (
        sum(float(r["국내후보"].get("점수", 0)) for r in rec if r["국내후보"])
        / max(covered, 1)
    )
    field_link = sum(1 for r in rec if r["현지파트너"]) / max(len(components), 1)

    # More intuitive than old formula: 40 coverage + 30 normalized domestic fit + 30 field linkage.
    domestic_fit_component = min(avg_match, 100) / 100 * 30
    total = round(coverage * 40 + domestic_fit_component + field_link * 30, 1)
    breakdown = {
        "분야 커버리지(40)": round(coverage * 40, 1),
        "국내 매칭 강도(30)": round(domestic_fit_component, 1),
        "현지 연계(30)": round(field_link * 30, 1),
    }
    return rec, total, breakdown



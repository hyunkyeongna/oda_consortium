def match_domestic_legacy(
    component: Dict[str, Any],
    humanitarian: pd.DataFrame,
    nonprofit: pd.DataFrame,
    recipient_country: str = "스리랑카",
    top_n: int = 6,
) -> pd.DataFrame:
    """Legacy rule-based matching. Kept as fallback when partner_db is absent."""
    kws = component.get("keywords", []) or []
    rows: List[Dict[str, Any]] = []

    if humanitarian is not None and not humanitarian.empty:
        hum = humanitarian.copy()
        hum["_txt"] = (
            hum.get("사업명_국문", "").astype(str) + " "
            + hum.get("사업명_영문", "").astype(str) + " "
            + hum.get("지역", "").astype(str) + " "
            + hum.get("담당부서", "").astype(str)
        )
        if "수행기관" in hum.columns:
            for org, g in hum.groupby("수행기관"):
                if not safe_str(org):
                    continue
                hits: set[str] = set()
                countries: set[str] = set()
                for _, rr in g.iterrows():
                    hits.update(_kw_hits(rr.get("_txt", ""), kws))
                    countries.add(safe_str(rr.get("국가명", "")))
                if not hits:
                    continue
                score = len(hits) * 3
                note = f"KOICA 수행실적 {len(g)}건"
                if recipient_country in countries:
                    score += 5
                    note += f" · {recipient_country} 경험"
                for sub in re.split(r"[,\u3001·/]", safe_str(org)):
                    sub = sub.strip()
                    if sub:
                        rows.append({
                            "기관": sub,
                            "점수": score,
                            "출처": "실적기반",
                            "근거": f"{note} · 키워드 {sorted(hits)}",
                        })

    if nonprofit is not None and not nonprofit.empty:
        npf = nonprofit.copy()
        npf["_txt"] = npf.get("단체명", "").astype(str) + " " + npf.get("주된사업", "").astype(str)
        for _, rr in npf.iterrows():
            hits = _kw_hits(rr.get("_txt", ""), kws)
            if not hits:
                continue
            score = len(hits) * 2
            note = ""
            if safe_str(rr.get("등록기관", "")) in RELEVANT_MINISTRIES:
                score += 4
                note = f"{rr.get('등록기관', '')} 등록"
            rows.append({
                "기관": rr.get("단체명", ""),
                "점수": score,
                "출처": "등록기반",
                "근거": f"{note} · 키워드 {hits}".strip(" ·"),
            })

    if not rows:
        return pd.DataFrame(columns=["기관", "점수", "출처", "근거"])
    df = pd.DataFrame(rows)
    df = df.sort_values("점수", ascending=False).drop_duplicates("기관").head(top_n)
    return df.reset_index(drop=True)


def match_domestic(
    component: Dict[str, Any],
    humanitarian: pd.DataFrame | None = None,
    nonprofit: pd.DataFrame | None = None,
    recipient_country: str = "스리랑카",
    top_n: int = 6,
    partner_db: Optional[Dict[str, pd.DataFrame]] = None,
) -> pd.DataFrame:
    """
    Public matching function used by app.py.

    Preferred path: Partner Master DB.
    Fallback path: legacy keyword matching against KOICA humanitarian + NPO registry.
    """
    if partner_db_available(partner_db or {}):
        out = match_domestic_from_partner_db(component, partner_db or {}, recipient_country, top_n=top_n)
        if not out.empty:
            return out
    return match_domestic_legacy(component, humanitarian if humanitarian is not None else pd.DataFrame(), nonprofit if nonprofit is not None else pd.DataFrame(), recipient_country, top_n)



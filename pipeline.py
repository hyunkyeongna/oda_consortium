def safe_str(value: Any) -> str:
    if value is None:
        return ""
    try:
        if pd.isna(value):
            return ""
    except Exception:
        pass
    return str(value).strip()


def read_csv_safely(path: Path) -> pd.DataFrame:
    if not path.exists():
        return pd.DataFrame()
    encodings = ["utf-8-sig", "utf-8", "cp949", "euc-kr"]
    last_error: Optional[Exception] = None
    for enc in encodings:
        try:
            return pd.read_csv(path, dtype=str, encoding=enc).fillna("")
        except Exception as exc:
            last_error = exc
    raise RuntimeError(f"Could not read CSV: {path}. Last error: {last_error}")


def find_existing_file(data_dir: Path, filename: str) -> Path:
    """Find CSV in data_dir first, then one level up. Useful for local demos."""
    direct = data_dir / filename
    if direct.exists():
        return direct
    parent = data_dir.parent / filename
    if parent.exists():
        return parent
    return direct


def _kw_hits(text: str, keywords: Sequence[str]) -> List[str]:
    t = safe_str(text).lower()
    hits = []
    for k in keywords:
        kk = safe_str(k).lower()
        if kk and kk in t:
            hits.append(k)
    return hits


def _to_int(value: Any, default: int = 0) -> int:
    text = re.sub(r"[^0-9\-]", "", safe_str(value))
    if not text:
        return default
    try:
        return int(text)
    except Exception:
        return default


def _to_float(value: Any, default: float = 0.0) -> float:
    text = re.sub(r"[^0-9\.\-]", "", safe_str(value))
    if not text:
        return default
    try:
        return float(text)
    except Exception:
        return default



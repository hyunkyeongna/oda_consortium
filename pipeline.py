def _evidence_blob(row: pd.Series) -> str:
    return (
        safe_str(row.get("evidence_keywords", "")) + " "
        + safe_str(row.get("evidence_summary", ""))
    ).lower()


def _precision_multiplier(row: pd.Series, component: Dict[str, Any]) -> float:
    """Reduce false positives from broad keywords such as ICT/data/center."""
    cap = safe_str(row.get("capability_tag"))
    text = component_text(component)
    blob = _evidence_blob(row)

    strong_terms = STRONG_TECH_TERMS.get(cap, [])
    if strong_terms and any(t.lower() in text for t in strong_terms):
        strong_hits = [t for t in strong_terms if t.lower() in blob]
        if not strong_hits:
            return 0.30
        if len(strong_hits) >= 2:
            return 1.20
        return 1.00

    return 1.00


def _component_keyword_overlap(row: pd.Series, component: Dict[str, Any]) -> float:
    """Small bonus for direct component-keyword evidence, excluding generic words."""
    blob = _evidence_blob(row)
    kws = [safe_str(k).lower() for k in component.get("keywords", []) or []]
    kws += [safe_str(component.get("name", "")).lower(), safe_str(component.get("sector", "")).lower()]

    hits = []
    for kw in kws:
        if not kw or kw in GENERIC_COMPONENT_TERMS or len(kw) < 2:
            continue
        if kw in blob:
            hits.append(kw)
    return min(10.0, len(set(hits)) * 2.5)



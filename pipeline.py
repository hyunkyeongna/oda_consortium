def load_partner_db(partner_db_dir: str | Path) -> Dict[str, pd.DataFrame]:
    """Load Partner Master DB generated by partner_master_builder.py."""
    p = Path(partner_db_dir)
    tables = {
        "partners": read_csv_safely(p / "partners.csv"),
        "summary": read_csv_safely(p / "partner_master_summary.csv"),
        "capabilities": read_csv_safely(p / "partner_capabilities.csv"),
        "projects": read_csv_safely(p / "partner_projects.csv"),
        "participants": read_csv_safely(p / "project_participants.csv"),
        "sources": read_csv_safely(p / "partner_sources.csv"),
        "aliases": read_csv_safely(p / "partner_aliases.csv"),
        "edges": read_csv_safely(p / "partner_edges.csv"),
        "procurement_records": read_csv_safely(p / "procurement_records.csv"),
    }
    return tables


def partner_db_available(partner_db: Dict[str, pd.DataFrame]) -> bool:
    return (
        isinstance(partner_db, dict)
        and not partner_db.get("summary", pd.DataFrame()).empty
        and not partner_db.get("capabilities", pd.DataFrame()).empty
    )


def load_data(data_dir: str | Path, partner_db_dir: str | Path | None = None) -> Dict[str, Any]:
    """Load raw source CSVs plus Partner Master DB if available."""
    data_dir = Path(data_dir)
    if partner_db_dir is None:
        # Normal project layout: project/data + project/partner_db.
        # Flat demo layout: /mnt/data + /mnt/data/partner_db.
        if (data_dir / "partner_db").exists():
            partner_db_dir = data_dir / "partner_db"
        else:
            partner_db_dir = data_dir.parent / "partner_db"
    else:
        partner_db_dir = Path(partner_db_dir)

    d: Dict[str, Any] = {}
    d["iati"] = read_csv_safely(find_existing_file(data_dir, "koica_iati.csv"))
    d["humanitarian"] = read_csv_safely(find_existing_file(data_dir, "koica_humanitarian.csv"))
    d["nonprofit"] = read_csv_safely(find_existing_file(data_dir, "nonprofit_national.csv"))
    d["partner_db"] = load_partner_db(partner_db_dir)
    return d



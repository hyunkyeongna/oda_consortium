def iati_country_footprint(iati: pd.DataFrame, country: str = "스리랑카") -> Tuple[pd.DataFrame, pd.Series, Dict[str, int]]:
    if iati is None or iati.empty:
        return pd.DataFrame(), pd.Series(dtype=int), {}
    if "수원국" not in iati.columns:
        return pd.DataFrame(), pd.Series(dtype=int), {}

    sl = iati[iati["수원국"].astype(str).str.contains(country, na=False)].copy()
    sectors = sl["사업분야명"].value_counts() if "사업분야명" in sl.columns else pd.Series(dtype=int)

    partners: Dict[str, int] = {}
    title_cols = [c for c in ["사업명(한글)", "사업명(영문)"] if c in sl.columns]
    for col in title_cols:
        for name in sl[col].astype(str).tolist():
            for m in MULTILATERAL_PAT.findall(name):
                key = m.upper().replace("-", "")
                partners[key] = partners.get(key, 0) + 1
    return sl, sectors, partners


def co_implementation_edges(humanitarian: pd.DataFrame | None = None, partner_db: Optional[Dict[str, pd.DataFrame]] = None) -> List[Tuple[str, str, str]]:
    """Extract verified / demo co-delivery edges.

    Priority:
    1) partner_db/partner_edges.csv generated by procurement ingest or mock seed.
    2) legacy humanitarian CSV where 수행기관 contains multiple organizations.

    Returned values are display names, because graph_view connects edges by visible node labels.
    """
    edges: List[Tuple[str, str, str]] = []

    # New path: Partner Master DB edges. partner_a / partner_b may be either
    # partner_id or display name. Resolve IDs to canonical Korean names when possible.
    if partner_db:
        edge_df = partner_db.get("edges", pd.DataFrame()).copy()
        summary = partner_db.get("summary", pd.DataFrame()).copy()
        name_map: Dict[str, str] = {}
        if not summary.empty and {"partner_id", "canonical_name_ko"}.issubset(summary.columns):
            for _, sr in summary.iterrows():
                pid = safe_str(sr.get("partner_id"))
                name = safe_str(sr.get("canonical_name_ko")) or safe_str(sr.get("canonical_name_en")) or pid
                if pid:
                    name_map[pid] = name

        if not edge_df.empty and {"partner_a", "partner_b"}.issubset(edge_df.columns):
            for _, r in edge_df.iterrows():
                a_raw = safe_str(r.get("partner_a"))
                b_raw = safe_str(r.get("partner_b"))
                if not a_raw or not b_raw:
                    continue
                a = name_map.get(a_raw, a_raw)
                b = name_map.get(b_raw, b_raw)
                title = safe_str(r.get("evidence_text")) or safe_str(r.get("project_id")) or "Co-delivery evidence"
                edges.append((a, b, title))

    # Legacy path: humanitarian CSV consortium notation.
    if humanitarian is not None and not humanitarian.empty and "수행기관" in humanitarian.columns:
        for _, r in humanitarian.iterrows():
            org = safe_str(r.get("수행기관", ""))
            parts = [p.strip() for p in re.split(r"[,\u3001·]", org) if p.strip()]
            if len(parts) > 1:
                title = safe_str(r.get("사업명_국문", "")) or safe_str(r.get("사업명_영문", ""))
                for i in range(len(parts)):
                    for j in range(i + 1, len(parts)):
                        edges.append((parts[i], parts[j], title))

    # Deduplicate while preserving order.
    seen = set()
    out: List[Tuple[str, str, str]] = []
    for a, b, title in edges:
        key = tuple(sorted([a, b]) + [title])
        if key not in seen:
            seen.add(key)
            out.append((a, b, title))
    return out



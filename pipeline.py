def match_domestic_from_partner_db(
    component: Dict[str, Any],
    partner_db: Dict[str, pd.DataFrame],
    recipient_country: str = "스리랑카",
    top_n: int = 6,
    domestic_only: bool = True,
) -> pd.DataFrame:
    """Rank partners against a component using Partner Master DB evidence."""
    summary = partner_db.get("summary", pd.DataFrame()).copy()
    caps = partner_db.get("capabilities", pd.DataFrame()).copy()
    projects = partner_db.get("projects", pd.DataFrame()).copy()

    if summary.empty or caps.empty:
        return pd.DataFrame(columns=["기관", "점수", "출처", "근거"])

    required = infer_required_capabilities(component)
    required_tags = [r["capability_tag"] for r in required]
    required_weights = {r["capability_tag"]: float(r["weight"]) for r in required}
    required_hit_map = {r["capability_tag"]: r.get("hits", []) for r in required}

    caps = caps[caps["capability_tag"].isin(required_tags)].copy()
    if caps.empty:
        return pd.DataFrame(columns=["기관", "점수", "출처", "근거"])

    # Normalize numeric fields.
    caps["score_num"] = caps["score"].apply(_to_float)
    caps["evidence_count_num"] = caps["evidence_count"].apply(_to_int)
    caps["last_evidence_year_num"] = caps["last_evidence_year"].apply(_to_int)
    caps["required_weight"] = caps["capability_tag"].map(required_weights).fillna(0.5)
    caps["precision_multiplier"] = caps.apply(lambda r: _precision_multiplier(r, component), axis=1)
    caps["keyword_overlap_bonus"] = caps.apply(lambda r: _component_keyword_overlap(r, component), axis=1)
    caps["capability_component_score"] = (
        caps["score_num"] * caps["required_weight"] * caps["precision_multiplier"]
        + caps["keyword_overlap_bonus"]
    )

    # Aggregate capability evidence per partner.
    agg = caps.groupby("partner_id", as_index=False).agg(
        capability_fit=("capability_component_score", "sum"),
        evidence_count=("evidence_count_num", "sum"),
        last_evidence_year=("last_evidence_year_num", "max"),
        matched_capabilities=("capability_tag", lambda s: ", ".join(dict.fromkeys(s.astype(str).tolist()))),
        evidence_keywords=("evidence_keywords", lambda s: ", ".join(sorted(set(", ".join(s.astype(str)).split(", "))))[:280]),
        evidence_summary=("evidence_summary", lambda s: " | ".join(s.astype(str).head(2).tolist())[:500]),
    )

    merged = agg.merge(summary, on="partner_id", how="left")

    # Filter out multilaterals for domestic matching, while keeping universities/NGOs/firms/etc.
    if domestic_only and "org_type" in merged.columns:
        intl_types = {"multilateral", "international_organization", "multilateral_development_bank"}
        merged = merged[~merged["org_type"].astype(str).isin(intl_types)].copy()

    # Country/regional bonus from project table.
    country_bonus_map: Dict[str, float] = {}
    country_evidence_map: Dict[str, str] = {}
    if not projects.empty and recipient_country:
        # Attach participants if available for project-to-partner relation.
        participants = partner_db.get("participants", pd.DataFrame()).copy()
        if not participants.empty:
            pp = participants.merge(projects[["project_id", "country", "project_title_ko"]], on="project_id", how="left")
            pp["country"] = pp["country"].fillna("")
            for pid, g in pp.groupby("partner_id"):
                exact = g[g["country"].astype(str).str.contains(recipient_country, na=False)]
                if len(exact):
                    country_bonus_map[pid] = 7.0
                    country_evidence_map[pid] = f"{recipient_country} project experience"

    merged["country_bonus"] = merged["partner_id"].map(country_bonus_map).fillna(0.0)
    merged["country_evidence"] = merged["partner_id"].map(country_evidence_map).fillna("")

    # Source/project credibility.
    merged["source_count_num"] = merged.get("source_count", 0)
    merged["project_count_num"] = merged.get("project_count", 0)
    merged["source_count_num"] = merged["source_count_num"].apply(_to_int)
    merged["project_count_num"] = merged["project_count_num"].apply(_to_int)

    # Recency bonus. Unknown year = 0.
    merged["recency_bonus"] = merged["last_evidence_year"].apply(lambda y: max(0, min(8, (_to_int(y) - 2018) * 1.0)))
    merged["evidence_bonus"] = merged["evidence_count"].apply(lambda x: min(12, _to_int(x) * 1.2))
    merged["source_bonus"] = merged["source_count_num"].apply(lambda x: min(5, x * 1.5))
    merged["project_bonus"] = merged["project_count_num"].apply(lambda x: min(8, x * 0.5))

    # Penalize generic over-broad institutions slightly when evidence is only from IATI title parsing.
    merged["intl_penalty"] = 0.0
    if "first_seen_source" in merged.columns:
        merged.loc[merged["first_seen_source"].astype(str).eq("koica_iati"), "intl_penalty"] = 2.0

    merged["raw_score"] = (
        merged["capability_fit"] * 0.55
        + merged["evidence_bonus"]
        + merged["recency_bonus"]
        + merged["source_bonus"]
        + merged["project_bonus"]
        + merged["country_bonus"]
        - merged["intl_penalty"]
    )
    merged["점수"] = merged["raw_score"].clip(lower=0, upper=100).round(1)

    # Build UI-compatible fields.
    rows: List[Dict[str, Any]] = []
    for _, r in merged.sort_values("점수", ascending=False).head(top_n).iterrows():
        caps_str = safe_str(r.get("matched_capabilities", ""))
        cap_list = [c.strip() for c in caps_str.split(",") if c.strip()]
        role = " + ".join([ROLE_BY_CAPABILITY.get(c, c) for c in cap_list[:2]]) or suggested_role(required)

        required_hits = []
        for cap in cap_list:
            required_hits.extend(required_hit_map.get(cap, []))
        required_hits = sorted(set(required_hits))[:8]

        source_bits = []
        if _to_int(r.get("project_count", 0)):
            source_bits.append(f"project evidence {_to_int(r.get('project_count', 0))}건")
        if _to_int(r.get("source_count", 0)):
            source_bits.append(f"source {_to_int(r.get('source_count', 0))}개")
        if safe_str(r.get("country_evidence", "")):
            source_bits.append(safe_str(r.get("country_evidence", "")))

        evidence_keywords = safe_str(r.get("evidence_keywords", ""))
        evidence_keywords = re.sub(r"\s+", " ", evidence_keywords).strip(" ,")
        evidence_summary = safe_str(r.get("evidence_summary", ""))
        evidence_summary = re.sub(r"\s+", " ", evidence_summary)

        reason_parts = [
            f"role={role}",
            f"capability={caps_str}",
        ]
        if required_hits:
            reason_parts.append(f"component keywords={required_hits}")
        if evidence_keywords:
            reason_parts.append(f"evidence keywords={evidence_keywords[:160]}")
        if evidence_summary:
            reason_parts.append(f"evidence={evidence_summary[:220]}")

        rows.append({
            "partner_id": safe_str(r.get("partner_id")),
            "기관": safe_str(r.get("canonical_name_ko")) or safe_str(r.get("canonical_name_en")) or safe_str(r.get("partner_id")),
            "점수": float(r.get("점수", 0)),
            "출처": "Partner Master DB" + (" · " + " · ".join(source_bits) if source_bits else ""),
            "근거": " · ".join(reason_parts),
            "추천역할": role,
            "역량태그": caps_str,
            "evidence_count": int(_to_int(r.get("evidence_count", 0))),
            "last_evidence_year": int(_to_int(r.get("last_evidence_year", 0))),
            "org_type": safe_str(r.get("org_type", "")),
            "legal_type": safe_str(r.get("legal_type", "")),
        })

    return pd.DataFrame(rows)



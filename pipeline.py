def component_text(component: Dict[str, Any]) -> str:
    pieces: List[str] = []
    for key in ["id", "name", "desc", "description", "sector"]:
        pieces.append(safe_str(component.get(key, "")))
    for k in component.get("keywords", []) or []:
        pieces.append(safe_str(k))
    return " ".join(pieces).lower()


def infer_required_capabilities(component: Dict[str, Any], top_n: int = 4) -> List[Dict[str, Any]]:
    """Infer capability tags required by a component."""
    text = component_text(component)
    raw_scores: Dict[str, Dict[str, Any]] = {}

    for cap, keywords in CAPABILITY_KEYWORDS.items():
        hits = _kw_hits(text, keywords)
        if hits:
            raw_scores[cap] = {
                "capability_tag": cap,
                "weight": min(1.0, 0.45 + 0.15 * len(set(hits))),
                "hits": sorted(set(hits), key=lambda x: str(x)),
            }

    sector = safe_str(component.get("sector", ""))
    for cap in SECTOR_TO_CAPABILITY_HINTS.get(sector, []):
        if cap not in raw_scores:
            raw_scores[cap] = {"capability_tag": cap, "weight": 0.65, "hits": [sector]}
        else:
            raw_scores[cap]["weight"] = min(1.0, raw_scores[cap]["weight"] + 0.2)
            raw_scores[cap]["hits"] = sorted(set(raw_scores[cap]["hits"] + [sector]))

    # Sector guardrail: prevent terms such as "education center" from turning a
    # construction component into an education component.
    sector_allowed = {
        "산림/ICT": {"forestry_restoration", "GIS_remote_sensing", "project_design_policy_research", "ODA_PMC"},
        "산림/환경": {"forestry_restoration", "project_design_policy_research", "GIS_remote_sensing", "ODA_PMC"},
        "수산/농업": {"aquaculture_livelihood", "project_design_policy_research", "capacity_building_training", "ODA_PMC"},
        "건축/인프라": {"construction_infrastructure", "procurement_equipment", "ODA_PMC"},
        "조달": {"procurement_equipment", "construction_infrastructure", "ODA_PMC"},
        "교육/역량강화": {"capacity_building_training", "education_youth", "ODA_PMC", "project_design_policy_research"},
    }.get(sector)
    if sector_allowed:
        raw_scores = {k: v for k, v in raw_scores.items() if k in sector_allowed}

    if not raw_scores:
        # Generic fallback for ODA projects.
        raw_scores["ODA_PMC"] = {"capability_tag": "ODA_PMC", "weight": 0.45, "hits": ["fallback"]}
        raw_scores["project_design_policy_research"] = {
            "capability_tag": "project_design_policy_research",
            "weight": 0.45,
            "hits": ["fallback"],
        }

    ordered = sorted(raw_scores.values(), key=lambda x: x["weight"], reverse=True)
    return ordered[:top_n]


def suggested_role(required_caps: List[Dict[str, Any]]) -> str:
    if not required_caps:
        return "Implementation partner"
    labels = [ROLE_BY_CAPABILITY.get(c["capability_tag"], c["capability_tag"]) for c in required_caps[:2]]
    return " + ".join(labels)


